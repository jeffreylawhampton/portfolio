export const getImages = (filter) => {
  if (Object.keys(allImages).includes(filter)) {
    return allImages[filter];
  } else {
    let imageArray = [];
    Object.keys(allImages).forEach((key) => {
      allImages[key].forEach((item) => imageArray.push(item));
    });
    return imageArray;
  }
};

export const getFeatured = () => {
  let featuredArray = [];
  Object.keys(allImages).forEach((key) => {
    const images = allImages[key];
    const feature = images.filter((item) => item.featured);
    feature.forEach((image) => featuredArray.push(image));
  });
  return featuredArray.sort((a, b) => b.rating - a.rating);
};

export const getCategories = () => {
  return Object.keys(allImages);
};
